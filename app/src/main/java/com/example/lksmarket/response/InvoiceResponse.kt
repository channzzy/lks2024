package com.example.lksmarket.response

import com.google.gson.annotations.SerializedName

data class InvoiceResponse(

	@field:SerializedName("data")
	val data: List<DataItem?>? = null,

	@field:SerializedName("message")
	val message: String? = null,

	@field:SerializedName("status")
	val status: Int? = null
)

data class DataItem(

	@field:SerializedName("updated_at")
	val updatedAt: String? = null,

	@field:SerializedName("user_id")
	val userId: Int? = null,

	@field:SerializedName("invoice_num")
	val invoiceNum: String? = null,

	@field:SerializedName("qty_total")
	val qtyTotal: Int? = null,

	@field:SerializedName("created_at")
	val createdAt: String? = null,

	@field:SerializedName("id")
	val id: Int? = null,

	@field:SerializedName("price_total")
	val priceTotal: Int? = null
)
